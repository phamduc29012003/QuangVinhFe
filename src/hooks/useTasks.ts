import { useTaskStore, useFilteredTasks } from '@/stores'

export const useTasks = () => {
  const {
    tasks,
    isLoading,
    error,
    filter,
    sortBy,
    sortOrder,
    fetchTasks,
    createTask,
    updateTask,
    deleteTask,
    toggleTask,
    setFilter,
    setSortBy,
    setSortOrder,
    clearError,
    setLoading,
  } = useTaskStore()

  const filteredTasks = useFilteredTasks()

  return {
    tasks,
    filteredTasks,
    isLoading,
    error,
    filter,
    sortBy,
    sortOrder,
    fetchTasks,
    createTask,
    updateTask,
    deleteTask,
    toggleTask,
    setFilter,
    setSortBy,
    setSortOrder,
    clearError,
    setLoading,
  }
}
